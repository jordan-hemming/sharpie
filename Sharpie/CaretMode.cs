namespace Sharpie;

/// <summary>
/// Defines the caret mode.
/// </summary>
public enum CaretMode
{
    /// <summary>
    /// The caret is invisible.
    /// </summary>
    Invisible = 0,

    /// <summary>
    /// The caret is visible.
    /// </summary>
    Visible = 1,

    /// <summary>
    /// The caret is very visible.
    /// </summary>
    VeryVisible = 2
}
